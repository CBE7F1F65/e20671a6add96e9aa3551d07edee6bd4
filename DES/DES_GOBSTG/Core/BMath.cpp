float sint(int angle)
{
	while(angle >= 36000)
		angle -= 36000;
	while(angle < 0)
		angle += 36000;
	if(angle <= 9000)
		return mathtable[angle/10].sin;
	if(angle <= 18000)
		return mathtable[(18000-angle)/10].sin;
	if(angle <= 27000)
		return -mathtable[(angle-18000)/10].sin;
	return -mathtable[(36000-angle)/10].sin;
}

float cost(int angle)
{
	return sint(angle + 9000);
}

float tant(int angle)
{
	while(angle > 18000)
		angle -= 18000;
	while(angle < 0)
		angle += 18000;
	if(angle < 9000)
		return mathtable[angle/10].tan;
	return -mathtable[(18000-angle)/10].tan;
}

